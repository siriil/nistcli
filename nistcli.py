def get_cves(vendor, product, version):
    cpe = f"cpe:2.3:a:{vendor}:{product}:{version}"
    url = f"https://services.nvd.nist.gov/rest/json/cves/2.0?cpeName={cpe}"
    response = requests.get(url)
    if response.status_code != 200:
        sys.exit(print(f"RESPONSE {response.status_code} GET {url}"))
    return response.json()

def process_results(data, language):
    results = []
    for item in data.get("vulnerabilities", []):
        cve = item["cve"]
        cve_id = cve["id"]
        published = cve.get("published", "-")
        if published != "-" and language == "es":
            published = datetime.strptime(published, "%Y-%m-%dT%H:%M:%S.%f").strftime("%d/%m/%Y")
        elif published != "-" and language == "en":
            published = datetime.strptime(published, "%Y-%m-%dT%H:%M:%S.%f").strftime("%m/%d/%Y")
        else:
            published = "-"
        cwe = cve.get("weaknesses", [])
        cwe_id = cwe[0]["description"][0]["value"] if cwe else "-"
        if language == "es":
            cwe_traducido = CWE_TRANSLATIONS_ES.get(cwe_id, cwe_id)
        elif language == "en":
            cwe_traducido = CWE_TRANSLATIONS_EN.get(cwe_id, cwe_id)
        else:
            cwe_traducido = CWE_TRANSLATIONS_ES.get(cwe_id, cwe_id)
        
        metrics = cve.get("metrics", {})
        base_score = "-"
        access_vector = "-"
        access_complexity = "-"

        try:
            cvssv31 = metrics.get("cvssMetricV31", [])
            if cvssv31:
                cvss_data_v31 = cvssv31[0]["cvssData"]
                base_score_v31 = cvss_data_v31.get("baseScore", "-")
                access_vector_v31 = cvss_data_v31.get("attackVector", "-").capitalize()
                access_complexity_v31 = cvss_data_v31.get("attackComplexity", "-").capitalize()
        except:
            pass
        try:
            cvssv2 = metrics.get("cvssMetricV2", [])
            if cvssv2:
                cvss_data_v2 = cvssv2[0]["cvssData"]
                base_score_v2 = cvss_data_v2.get("baseScore", "-")
                access_vector_v2 = cvss_data_v2.get("accessVector", "NAN").capitalize()
                access_complexity_v2 = cvss_data_v2.get("accessComplexity", "NAN").capitalize()
        except:
            pass

        if cvssv31 != []:
            base_score = base_score_v31
            access_vector = access_vector_v31
            access_complexity = access_complexity_v31
        else:
            base_score = base_score_v2
            access_vector = access_vector_v2
            access_complexity = access_complexity_v2

        access_vector = ACCESS_TRANSLATIONS_EN.get(access_vector, access_vector) if language == "en" else ACCESS_TRANSLATIONS_ES.get(access_vector, access_vector)
        access_complexity = access_complexity if language == "en" else ACCESS_TRANSLATIONS_ES.get(access_complexity, access_complexity)

        results.append({
            "ID": cve_id,
            "CWE": cwe_traducido,
            "Published": published,
            "Base Score": base_score,
            "Access Vector": access_vector,
            "Access Complexity": access_complexity
        })
    results_criticos = sorted(results, key=lambda x: x["Base Score"], reverse=True)
    return results_criticos

def output_stdout(vendor, product, version, language, results):
    if language == "es":
        title = f"[+] {len(results)} CVEs para {vendor} {product} {version}"
    else:
        title = f"[+] {len(results)} CVEs for {vendor} {product} {version}"
    print(f"\n{title}\n")
    tabla = PrettyTable()
    if language == "es":
        tabla.field_names = ["CVE", "Vulnerabilidad", "Fecha de Publicación", "Puntuación", "Acceso", "Complejidad"]
    else:
        tabla.field_names = ["CVE", "Vulnerability", "Published Date", "Score", "Access", "Complexity"]
    for r in results:
        tabla.add_row([r["ID"], r["CWE"], r["Published"], r["Base Score"], r["Access Vector"], r["Access Complexity"]])
    print(tabla)
    print()

