def main():
    parser = argparse.ArgumentParser(description="Lookup CVEs in NIST NVD")
    parser.add_argument("-vr", required=True, help="Vendor", metavar="<VENDOR>")
    parser.add_argument("-p", required=True, help="Product", metavar="<PRODUCT>")
    parser.add_argument("-vp", required=True, help="Version", metavar="<VERSION PRODUCT>")
    parser.add_argument("-lg", required=True, choices=["es", "en"], help="Language ['es', 'en']", metavar="<es,en>")
    parser.add_argument("-oSTD", action="store_true", help="Output in STDOUT")
    parser.add_argument("-oHTML", nargs="?", const="", help="Output in HTML")

    args = parser.parse_args()
    vendor = args.vr
    product = args.p
    version = args.vp
    language = args.lg

    data = get_cves(vendor, product, version)
    results = process_results(data, language)

    if len(results) == 0:
        if language == "es":
            sys.exit(print(f"[-] No se encontraron CVEs para {vendor} {product} {version}"))
        else:
            sys.exit(print(f"[-] No CVEs found for {vendor} {product} {version}"))

    if not args.oSTD and args.oHTML is None:
        output_stdout(vendor, product, version, language, results)
        now = datetime.now().strftime("%d%m%Y%H%M%S")
        filename = f"report_{vendor}_{product}_{version}_{now}.html"
        output_html(vendor, product, version, language, results, filename)

    if args.oSTD:
        output_stdout(vendor, product, version, language, results)

    if args.oHTML is not None:
        if args.oHTML == "":
            now = datetime.now().strftime("%d%m%Y%H%M%S")
            filename = f"report_{vendor}_{product}_{version}_{now}.html"
        else:
            filename = args.oHTML
        output_html(vendor, product, version, language, results, filename)

if __name__ == "__main__":
    main()
